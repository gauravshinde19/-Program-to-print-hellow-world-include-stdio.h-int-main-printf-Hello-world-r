# Program to accept two integers from user and perform arithmetic operation on that numbers

num1 = int(input("Enter the first number :\n"))
num2 = int(input("Enter the second number :\n"))

print("\nAddition of two numbers:\n",num1 + num2)
print("Subtraction of two numbers:\n",num1 - num2)
print("Multiplication of two numbers:\n",num1 * num2)
print("Division of two numbers:\n",round(num1 / num2,2))

